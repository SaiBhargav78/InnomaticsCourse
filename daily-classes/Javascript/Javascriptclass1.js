let age = 12;
let height = 6;

// Addition
console.log("Addition:",age+height);

// Subtraction
console.log("Subtraction:",age-height);

// Multiplication
console.log("Multiplication:",age*height);

// Division
console.log("Division:",age/height);

// Modulus
console.log("Modulus:",age%height);

// Age != height
console.log("Age != height:",age!=height);

// Age > height
console.log("Age > height:",age>height);

// Age < height
console.log("Age < height:",age<height);

// Age == height
console.log("Age == height:",age==height);

// Age >= height
console.log("Age >= height:",age>=height);

// Age <= height
console.log("Age <= height:",age<=height);

// preincrement
console.log("Preincrement(++Age):",++age);
console.log("Age:",age);

// postincrement
console.log("Postincrement(Age++):",age++);
console.log("Age:",age);

// predecrement
console.log("Predecrement(--Age):",--age);
console.log("Age:",age);

// postdecrement
console.log("Postdecrement(Age--):",age--);
console.log("Age:",age);

